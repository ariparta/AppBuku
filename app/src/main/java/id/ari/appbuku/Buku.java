package id.ari.appbuku;

public class Buku {
    String id;
    String nama_buku;
    String kategori;
    String genre_buku;
    String penulis_buku;
    String rating_buku;
    String tanggal;
    String sinopsis_buku;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama_buku() {
        return nama_buku;
    }

    public void setNama_buku(String nama_buku) {
        this.nama_buku = nama_buku;
    }

    public String getKategori() {
        return kategori;
    }

    public void setKategori(String kategori) {
        this.kategori = kategori;
    }

    public String getGenre_buku() {
        return genre_buku;
    }

    public void setGenre_buku(String genre_buku) {
        this.genre_buku = genre_buku;
    }

    public String getPenulis_buku() {
        return penulis_buku;
    }

    public void setPenulis_buku(String penulis_buku) {
        this.penulis_buku = penulis_buku;
    }

    public String getRating_buku() {
        return rating_buku;
    }

    public void setRating_buku(String rating_buku) {
        this.rating_buku = rating_buku;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getSinopsis_buku() {
        return sinopsis_buku;
    }

    public void setSinopsis_buku(String sinopsis_buku) {
        this.sinopsis_buku = sinopsis_buku;
    }
}
